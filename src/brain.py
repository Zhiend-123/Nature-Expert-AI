import google.generativeai as genai


def setup_ai(api_key):
    """Инициализация Google Gemini"""
    genai.configure(api_key=api_key)


def get_nature_analysis(model_name, image):
    """Отправка изображения в ИИ и получение ответа"""
    model = genai.GenerativeModel(model_name)

    prompt = """
    Ты — эксперт-натуралист мирового уровня. Твоя задача — идентифицировать объект на фото.
    Если на фото НЕ птица, НЕ гриб и НЕ минерал, прямо скажи об этом.

    Отвечай СТРОГО по шаблону:
    ### 🎯 Идентификация
    - **ТИП:** (Птица / Гриб / Минерал)
    - **НАЗВАНИЕ:** (Русское название + *Latin Name*)
    - **УВЕРЕННОСТЬ:** (Укажи вероятность в % на основе визуальных признаков)

    ### 🔍 Детальный разбор
    - **Признаки:** (Что именно на фото подтверждает этот вид?)
    - **Ареал/Среда:** (Где встречается?)
    - **Особенности:** (Для грибов — съедобность, для минералов — твердость, для птиц — поведение)

    ### ⚠️ Важное замечание
    (Если это гриб — напиши предупреждение капсом. Если нет — напиши общую рекомендацию по сохранению природы.)
    """

    response = model.generate_content([prompt, image])
    return response.text